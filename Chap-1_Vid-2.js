// Variables in Javascript 
var a = 67 // a containes 67
console.log(a)
a = "Siddhesh"
console.log(a)
// here, in line no.2 we have stored a number 67 in 'a' and after at line no.4 we have stored a string "Siddhesh" in 'a', console.log(a) is printing both the values of 'a' as output because JS allows to change type of variable during runtime.